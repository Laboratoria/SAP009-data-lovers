import data from './data/ghibli/ghibli.js';

const mostrarCard = document.querySelector(".cards")

function showPeople() {
  mostrarCard.innerHTML = ""
  for (let i=0; i < data.films.length ; i++) {
    for (let j = 0; j <data.films[i].people.length; j++) {
      mostrarCard.innerHTML += `<div class="cards">
    <image class="card_image" src="${data.films[i].people[j].img}">
    <h3 class="card_nome"> ${data.films[i].people[j].name}</h3>`
    }
  }
}

showPeople(); 



